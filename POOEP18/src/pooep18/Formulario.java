    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        int codigo;
        double nHorasMes, pagoHora, porcentajeRetencion, salarioBruto, salarioNeto;
        String nombres;
        
        nHorasMes = Double.parseDouble( txtHorasMes.getText() );
        pagoHora = Double.parseDouble( txtPagoHora.getText() );
        porcentajeRetencion = Double.parseDouble( txtPorcentajeRetencion.getText() );
        salarioBruto = nHorasMes*pagoHora;
        salarioNeto = salarioBruto*(1 - porcentajeRetencion/100);
        txtSalarioBruto.setText(String.valueOf(salarioBruto));
        txtSalarioNeto.setText(String.valueOf(salarioNeto));
        
        
    }//GEN-LAST:event_btnCalcularActionPerformed

